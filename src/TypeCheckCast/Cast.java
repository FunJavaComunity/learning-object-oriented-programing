package TypeCheckCast;

public class Cast {
    public static void main(String[] args) {
        // Konversi tipe data dari double ke int
        double angkaDouble = 10.5;
        int angkaInt = (int) angkaDouble;
        System.out.println("Angka double: " + angkaDouble);
        System.out.println("Angka int: " + angkaInt);
    }
}
